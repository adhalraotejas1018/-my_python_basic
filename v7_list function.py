#lets learn the lists function and features
list18=["rutvik","virat",18,10,"viru","1018"]

#printing the list
print(list18)

#print only 0 to 2 element
print(list18[0:3])

#print list reversed
print(list18.reverse())
print(list18[::-1])


#inset/add element in list--- adding "hi at 0 poosition
list18.insert(0,"hi")
print(list18)

#append / insert element at last position on list
list18.append("bye bye")

#removing the element from string
list18.remove("bye bye")
print(list18)

